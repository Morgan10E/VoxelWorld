// Is called whenever a key is pressed/released via GLFW
void key_callback(GLFWwindow* window, int key, int scancode, int action, int mode)
{
    if (key == GLFW_KEY_ESCAPE && action == GLFW_PRESS)
        glfwSetWindowShouldClose(window, GL_TRUE);
    if (key >= 0 && key < 1024)
    {
        if (action == GLFW_PRESS)
            keys[key] = true;
        else if (action == GLFW_RELEASE)
            keys[key] = false;
    }
}

void do_movement()
{
    // Camera controls
    if (keys[GLFW_KEY_W])
        camera.ProcessKeyboard(FORWARD, deltaTime);
    if (keys[GLFW_KEY_S])
        camera.ProcessKeyboard(BACKWARD, deltaTime);
    if (keys[GLFW_KEY_A])
        camera.ProcessKeyboard(LEFT, deltaTime);
    if (keys[GLFW_KEY_D])
        camera.ProcessKeyboard(RIGHT, deltaTime);
    if (keys[GLFW_KEY_Q])
        camera.ProcessKeyboard(DOWN, deltaTime);
    if (keys[GLFW_KEY_E])
        camera.ProcessKeyboard(UP, deltaTime);
}

bool firstMouse = true;
void mouse_callback(GLFWwindow* window, double xpos, double ypos)
{
    if (firstMouse)
    {
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }

    GLfloat xoffset = xpos - lastX;
    GLfloat yoffset = lastY - ypos;  // Reversed since y-coordinates go from bottom to left

    lastX = xpos;
    lastY = ypos;

    camera.ProcessMouseMovement(xoffset, yoffset);
}

void scroll_callback(GLFWwindow* window, double xoffset, double yoffset)
{
    camera.ProcessMouseScroll(yoffset);
}

std::vector<std::vector<float> > perlinField(int width, int length, float heightMultiplier, float variability) {
  std::vector<std::vector<float> > grid (width);
  for (int r = 0; r < width; r++) {
    grid[r] = std::vector<float> (length);
    for (int c = 0; c < length; c++) {
      grid[r][c] = stb_perlin_noise3((float)r/width * variability, (float)c/length * variability, 0) * heightMultiplier;
    }
  }

  return grid;
}

void printGrid(std::vector<std::vector<float> >& grid) {
  for (std::vector<std::vector<float> >::iterator rit = grid.begin(); rit != grid.end(); ++rit) {
    for (std::vector<float>::iterator cit = (*rit).begin(); cit != (*rit).end(); ++cit) {
      std::cout << ' ' << *cit;
    }
    std::cout << '\n';
  }
  std::cout << '\n';
}
